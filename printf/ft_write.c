#include "../minitalk.h"

int	ft_write(char *s)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s[i] && j >= 0)
	{
		j = write(1, &s[i], 1);
		i++;
	}
	if (j < 0)
		return (-2147483648);
	return (i);
}
